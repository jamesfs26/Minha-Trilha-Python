
# pop([i]): Remove e retorna o item na posição especificada.
# Se o índice não for especificado, remove e retorna o último item da lista. 
# Configuração Padrão: Star = 0 | Stop = Final | Step = 1

Alfabeto = ['a', 'b', 'c', 'd', 'e']

item1 = Alfabeto.pop()

item2 = Alfabeto.pop(0)


print(item1)
print(item2)
print(Alfabeto)















