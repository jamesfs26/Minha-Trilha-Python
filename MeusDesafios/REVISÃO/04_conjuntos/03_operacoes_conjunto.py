
# União

conjunto1 = {1, 2, 3}
conjunto2 = {4, 5, 6}
uniao = conjunto1.union(conjunto2)
print(uniao)

# Interseção

conjunto3 = {10, 20, 30}
conjunto4 = {40, 50, 60}
juncao = conjunto3.intersection(conjunto4)
print(juncao)

# Diferença

diferenca = conjunto3.difference(conjunto4)
print(diferenca)

# Diferença Simétria

diferenca_simetrica = conjunto3.symmetric_difference(conjunto4)




















