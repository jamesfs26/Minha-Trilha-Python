
#Existem várias formas de se criar um dicionário vazio em Python:

    #**Usando o operador {}: my_dict = {}
   # **Usando o método dict(): my_dict = dict()
    #**Usando o método dict.fromkeys(): my_dict = dict.fromkeys([])
    #**Usando o método dict.fromkeys() com uma lista de chaves: my_dict = dict.fromkeys([1, 2, 3, 4, 5])

     

# 1. Usando o construtor do dicionário
meu_dicionario1 = {}
print(meu_dicionario1)

# 2. Usando o operador {}
meu_dicionario2 = {}
print(meu_dicionario2)

# 3. Usando o método dict()
meu_dicionario3 = dict()
print(meu_dicionario3)

# 4. Usando o método dict.fromkeys()
meu_dicionario4 = dict.fromkeys([])
print(meu_dicionario4)

# 5. Usando o método dict.fromkeys() com uma lista de chaves
meu_dicionario5 = dict.fromkeys([1, 2, 3, 4, 5])
print(meu_dicionario5)














