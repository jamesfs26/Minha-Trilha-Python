
cidades = ["Parazinho", "Pedra Grande", "João Câmara", "Caiçara do Norte", "São Bento do Norte", "Bento Fernandes", "Jandaira"]

print("Lista Sem Iteração:")
print(cidades)

print(" ")

print("Lista com Iteração:")
for cidades in cidades:
    print(cidades)

# Ela organiza os valores da lista em uma sequência de cascata, melhorando a vizualização.














